# Concatenate Strings with "+"
# Don't forget the space!
first_name = "ada"
last_name = "lovelace"
full_name = first_name + " " + last_name

greeting = "Hello, " + full_name.title() + "!"
print(greeting)

# tabs are expressed using "\t" within the quotes
print("Python")
print("\tPython")

# new lines are expressed using "\n" inside the quotes
print("Languages:\n\tPython\n\tC\n\tJavaScript")
